    public void loadData() throws SQLException {
        String[] column = {"Mã Nhân Viên", "Tên Nhân Viên", "Địa chỉ", "Mật Khẩu", "Quyền"};
        DefaultTableModel tableModel = new DefaultTableModel(column, 0);
        ResultSet rs = (ResultSet) model.get();
        while (rs.next()) {
            String role = "";
            if (rs.getInt(5) == 0) {
                role = "User";
            } else {
                role = "Manager";
            }
            tableModel.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), role});
        }
        jtbNhanVien.setModel(tableModel);
    }

