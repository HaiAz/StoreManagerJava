    private void jtbNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbNhanVienMouseClicked
        // TODO add your handling code here:
        int row = jtbNhanVien.getSelectedRow();
        if (row >= 0) {
            txtMaNV.setText(jtbNhanVien.getModel().getValueAt(row, 0).toString());
            txtTenNV.setText(jtbNhanVien.getModel().getValueAt(row, 1).toString());
            txtDC.setText(jtbNhanVien.getModel().getValueAt(row, 2).toString());
            txtMatKhau.setText(jtbNhanVien.getModel().getValueAt(row, 3).toString());
            if (jtbNhanVien.getModel().getValueAt(row, 4).toString().equals("Manager")) {
                jcbQuyen.setSelectedIndex(0);
            } else {
                jcbQuyen.setSelectedIndex(1);
            }
        }
    }//GEN-LAST:event_jtbNhanVienMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        NhanVien nv = new NhanVien();
        if (txtMaNV.getText().trim().equals("") || txtMaNV.getText().trim().equals("") || txtMaNV.getText().trim().equals("") || txtMaNV.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ thông tin", "Thông báo", 0);
            return;
        }
        nv.setMaNV(txtMaNV.getText());
        nv.setTenNV(txtTenNV.getText());
        nv.setMatKhau(txtMatKhau.getText());
        nv.setDiaChi(txtDC.getText());
        if (jcbQuyen.getSelectedIndex() == 0) {
            nv.setQuyen(true);
        } else {
            nv.setQuyen(false);
        }
        try {
            model.insert(nv);
            loadData();
            JOptionPane.showMessageDialog(this, "Thêm thành công", "Thông báo", 1);

        } catch (SQLException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

