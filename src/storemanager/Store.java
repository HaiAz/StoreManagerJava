    private void jtbMatHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbMatHangMouseClicked
        // TODO add your handling code here:
        int row = jtbMatHang.getSelectedRow();
        txtMaHang.setText(jtbMatHang.getModel().getValueAt(row, 0).toString());
        txtTenHang.setText(jtbMatHang.getModel().getValueAt(row, 1).toString());
        txtDVT.setText(jtbMatHang.getModel().getValueAt(row, 2).toString());
        txtDongia.setText(jtbMatHang.getModel().getValueAt(row, 3).toString());
        int index = 0;
        for (int i = 0; i < cbLoaiHang.getItemCount(); i++) {
            LoaiHang lh = cbLoaiHang.getModel().getElementAt(i);
            if (lh.getTenLoai().equals(jtbMatHang.getModel().getValueAt(row, 4).toString())) {
                index = i;
            }
            cbLoaiHang.setSelectedIndex(index);
        }
    }//GEN-LAST:event_jtbMatHangMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        CartModel cartItem = new CartModel(txtMaHang.getText(), txtTenHang.getText(), Integer.parseInt(txtSoluong.getText()), Integer.parseInt(txtDongia.getText()), Integer.parseInt(txtDongia.getText()) * Integer.parseInt(txtSoluong.getText()));
        if (cart.size() < 1) {
            cart.add(cartItem);
            try {
                Cart.loadData();
            } catch (SQLException ex) {
                Logger.getLogger(Store.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            int check = 0;
            for (int i = 0; i < cart.size(); i++) {
                if (cart.get(i).getMaHang().equals(txtMaHang.getText())) {
                    check = 1;
                    cart.get(i).setSoLuong(cart.get(i).getSoLuong() + Integer.parseInt(txtSoluong.getText()));
                    cart.get(i).setTongTien(cart.get(i).getSoLuong() * cartItem.getDonGia());
                    try {
                        Cart.loadData();
                    } catch (SQLException ex) {
                        Logger.getLogger(Store.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    break;
                }
            }
            if (check == 0) {
                cart.add(cartItem);
                try {
                    Cart.loadData();
                } catch (SQLException ex) {
                    Logger.getLogger(Store.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed


