    public void loadData() throws SQLException {
        String[] column = {"Mã hàng", "Tên hàng", "ĐVT", "Đơn Giá", "Loại Hàng"};
        DefaultTableModel tableModel = new DefaultTableModel(column, 0);
        ResultSet rs = (ResultSet) model.get();
        while (rs.next()) {
            tableModel.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getInt(4), rs.getString(5)});
        }
        DefaultComboBoxModel<LoaiHang> cbModel = new DefaultComboBoxModel();

        ResultSet rsLoaiHang = new LoaiHangModel().get();
        while (rsLoaiHang.next()) {
            cbModel.addElement(new LoaiHang(rsLoaiHang.getString(1), rsLoaiHang.getString(2)));
        }
        cbLoaiHang.setModel(cbModel);
        jtbMatHang.setModel(tableModel);
    }

