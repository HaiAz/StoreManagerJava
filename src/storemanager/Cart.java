    public static void loadData() throws SQLException {
        String[] column = {"Mã Hàng", "Tên Hàng", "Số Lượng", "Đơn giá", "Tổng tiền"};
        DefaultTableModel tableModel = new DefaultTableModel(column, 0);
        for (int i = 0; i < Store.cart.size(); i++) {
            CartModel cart = Store.cart.get(i);
            tableModel.addRow(new Object[]{cart.getMaHang(), cart.getTenHang(), cart.getSoLuong(), cart.getDonGia(), cart.getTongTien()});
        }
        DefaultComboBoxModel<KhachHang> cbkhModel = new DefaultComboBoxModel<KhachHang>();
        ResultSet khachHangRs = model.get();
        while (khachHangRs.next()) {
            cbkhModel.addElement(new KhachHang(khachHangRs.getString(1), khachHangRs.getString(2)));
        }
        cbKhachHang.setModel(cbkhModel);
        jtbLoaiHang.setModel(tableModel);
    }

