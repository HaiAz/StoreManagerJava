    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            // TODO add your handling code here:
            if(txtMaHoaDon.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Chưa nhập mã hoá đơn","Thông báo",0);
                return;
            }
            String maHoaDon = txtMaHoaDon.getText();
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            LocalDateTime now = LocalDateTime.now();
            DB db = new DB();
            String sql = "insert into hoadon values(?,?,?,?)";
            PreparedStatement ps = db.getConnection().prepareStatement(sql);
            ps.setString(1, maHoaDon);
            ps.setString(2, dtf.format(now));
            ps.setString(3, cbKhachHang.getModel().getElementAt(cbKhachHang.getSelectedIndex()).getMaKH());
            ps.setString(4, Login.maNV);
            ps.execute();
            for (int i = 0; i < Store.cart.size(); i++) {
                String sql1 = "insert into cthoadon values(?,?,?,?)";
                PreparedStatement ps1 = db.getConnection().prepareStatement(sql1);
                ps1.setString(1, maHoaDon);
                ps1.setString(2, Store.cart.get(i).getMaHang());
                ps1.setInt(3, Store.cart.get(i).getSoLuong());
                ps1.setInt(4, Store.cart.get(i).getDonGia());
                ps1.execute();
            }
            JOptionPane.showMessageDialog(this, "Lập hoá đơn thành công!", "Thông báo", 1);
            CT_HoaDon ctHoaDon = new CT_HoaDon(maHoaDon);
            ctHoaDon.show();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lập hoá đơn thất bại! Mã hoá đơn đã tồn tại", "Lỗi", 0);
        }
    }//GEN-LAST:event_btnThemActionPerformed


